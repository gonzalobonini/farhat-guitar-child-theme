<h4>To install the lessons in your computer follow the instructions:</h4>
<ol>
 	<li>Please <strong>download the file</strong></li>
 	<li><strong>Extract the file</strong> anywhere in your disk</li>
 	<li>A folder with the name of the song will be created</li>
 	<li><strong>Double click on the folder</strong> created and you will see the file "index".</li>
 	<li>Right click over the <strong>¨index¨ file</strong> and a menu will appear</li>
 	<li>Choose the option ¨open with..¨ and a list of browsers (depending on which ones you have installed) will be available to choose.</li>
 	<li>Which one to choose?
If you download the Firefox/Chrome version choose Firefox or Chrome If you download the IE/Safari version choose Internet Explorer or Safari
* If you download the All Browser version choose any of them</li>
 	<li>After you choose the browser, the selected browser will be open and you will see a page where you could navigate the lessons you have bought, the lessons will appear on the left, the video on the right and the tab on the top.</li>
</ol>

